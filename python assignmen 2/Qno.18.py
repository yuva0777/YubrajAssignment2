city = input("Enter the name of a city (Delhi, Agra, Jaipur): ")

if city == "Delhi":
    print("The monument in Delhi is Red Fort.")
elif city == "Agra":
    print("The monument in Agra is Taj Mahal.")
elif city == "Jaipur":
    print("The monument in Jaipur is Jal Mahal.")
else:
    print("Sorry, information for this city is not available.")
